function ListGroup() {
  const items = ["London", "New York", "Paris", "Tokyo", "San Francisco"];

  return (
    <>
      <h1>List</h1>
      <ul className="list-group">
        {items.map((item) => (
          <li key={item}>{item}</li>
        ))}
      </ul>
    </>
  );
}

export default ListGroup;
